    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        StudentEntry student = (StudentEntry) dropStudentComboBox.getSelectedItem();
        dropStudentTextArea.append(student + "\n\n");

        ArrayList<String> semesters = SemesterQueries.getSemesterList();

        for (String semester : semesters) {
            dropStudentTextArea.append("For Semester: " + semester + "\n");

            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, student.getStudentID());

            for (ScheduleEntry schedule : schedules) {
                String courseCode = schedule.getCourseCode();
                ScheduleQueries.dropStudentScheduleByCourse(semester, schedule.getStudentID(), courseCode);
                dropStudentTextArea.append(student +" "+student.getStudentID()+ " has been dropped from " + courseCode + "\n");

                ArrayList<ScheduleEntry> waitlistedEntries = ScheduleQueries.getWaitlistedStudentsByClass(semester, courseCode);
                if (!waitlistedEntries.isEmpty()) {
                    ScheduleQueries.updateScheduleEntry(waitlistedEntries.get(0));
                    StudentEntry waitlistedStudent = StudentQueries.getStudent(waitlistedEntries.get(0).getStudentID());
                    dropStudentTextArea.append(waitlistedStudent +" "+waitlistedStudent.getStudentID()+ " has been dropped from the waitlist for " + courseCode + "\n");
                }
            }
            dropStudentTextArea.append("\n");

        }

        StudentQueries.dropStudent(student.getStudentID());
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_jButton5ActionPerformed

