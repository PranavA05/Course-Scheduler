    private void addClassDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClassDisplayButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<ClassDescription> classes = MultiTableQueries.getAllClassDescriptions(currentSemester);

    // Populate text area
    displayCoursesTextarea.setText("Course Code\tDescription\tSeats\n");
    if (classes.isEmpty()) {
        displayCoursesTextarea.append("No classes available for the selected semester.\n");
    } else {
        for (ClassDescription classEntry : classes) {
            displayCoursesTextarea.append(classEntry.getCourseCode() + "\t" + classEntry.getDescription() + "\t" + classEntry.getSeats() + "\n");
        }
    }

    // Populate table
    DefaultTableModel tableModel = (DefaultTableModel) displayCoursesTable.getModel();
    tableModel.setNumRows(0); // Clear existing rows
    Object[] values = new Object[3];
    for (ClassDescription c : classes) {
        values[0] = c.getCourseCode();
        values[1] = c.getDescription();
        values[2] = c.getSeats();
        tableModel.addRow(values);
        }
    }//GEN-LAST:event_addClassDisplayButtonActionPerformed

    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = (String) selectclassComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry) selectStudentComboBox.getSelectedItem();
        Date date = new Date();
        Timestamp timestamp = new Timestamp(date.getTime());
        String status = "";


        if (ScheduleQueries.getScheduledStudentCount(currentSemesterLabel.getText(), courseCode) >= ClassQueries.getClassSeats(currentSemesterLabel.getText(), courseCode)) {
            status = "waitlisted";
        } else {
            status = "scheduled";
        }

        ScheduleEntry schedule = new ScheduleEntry(currentSemesterLabel.getText(), courseCode, student.getStudentID(), status, timestamp);
        ScheduleQueries.addScheduleEntry(schedule);
        

        addScheduleClassStatusLabel.setText(student + " has been " + status + " for the class."); 
        
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed

    private void displayScheduleDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleDisplayButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry selectedStudent = (StudentEntry) scheduleSelectStudentComboBox.getSelectedItem();

        // Extract first and last name from the selected student
        String lastName = selectedStudent.getLastName();
        String firstName = selectedStudent.getFirstName();

    // Get the student ID using the last and first name
    String studentID = StudentQueries.getStudentID(lastName, firstName);

    // Retrieve the schedule for the student
    ArrayList<ScheduleEntry> student = ScheduleQueries.getScheduleByStudent(currentSemester, studentID);

    // Set up the table model for displaying schedule
    DefaultTableModel tabeleModel = (DefaultTableModel) displayScheduleTable.getModel();

    // Clear and repopulate the text area
    displayScheduleTextArea.setText("Course Code\tStatus\n");
    for (ScheduleEntry scheduleEntry : student) {
        displayScheduleTextArea.append(scheduleEntry.getCourseCode() + "\t" + scheduleEntry.getStatus() + "\n");
    }

    // Clear and repopulate the table
    tabeleModel.setNumRows(0);
    Object[] values = new Object[2];
    for (ScheduleEntry c : student) {
        values[0] = c.getCourseCode(); //Column-1
        values[1] = c.getStatus();     //Column-2
        tabeleModel.addRow(values);
        }          
    }//GEN-LAST:event_displayScheduleDisplayButtonActionPerformed

